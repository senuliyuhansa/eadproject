    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                // Get the department name and location from text fields
    String departmentName = jTextField2.getText();
    String location = jTextField3.getText();

    // Perform input validation if needed

    // Register the department in the database
    try {
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ems", "root", "");

        String sql = "INSERT INTO Department (DepartmentName, Location) VALUES (?, ?)";
        PreparedStatement preparedStatement = connection.prepareStatement(sql);
        preparedStatement.setString(1, departmentName);
        preparedStatement.setString(2, location);

        int rowsAffected = preparedStatement.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Department registration successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Department registration failed.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        preparedStatement.close();
        connection.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

