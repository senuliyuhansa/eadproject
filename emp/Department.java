    private void generateAndDepartmentID() {
        try {
            Connection connection = DriverManager.getConnection(jdbcUrl, dbUsername, dbPassword);
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT MAX(DepartmentID) FROM Department");

            int nextEmployeeID = 1; // Default value if no records exist
            if (resultSet.next()) {
                int maxEmployeeID = resultSet.getInt(1);
                nextEmployeeID = maxEmployeeID + 1;
            }

            jTextField1.setText(String.valueOf(nextEmployeeID));
            
            resultSet.close();
            statement.close();
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

